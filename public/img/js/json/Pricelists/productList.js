productTypes = ['All']
let selectedProduct = "Geotextiles"
const results = document.querySelector('#productResults')

function productSearch(term, parameter) {
    let items = Object.entries(productList[selectedProduct])

    for (i = 0; i < items.length; i++) {
        if (items[i][1][`${parameter}`].toLowerCase().includes(term.toLowerCase())) {
            let item = items[i][1]
            clearProducts()
            renderProduct(0, item, results)
        } else {
            clearProducts()
        }
    }
}

function filterProductType(filter) {
    let items = Object.entries(productList[selectedProduct])

    if (filter != "All") {
        for (i = 0; i < items.length; i++) {
            if (items[i][1].type.includes(filter)) {
                let item = items[i][1]
                clearProducts()
                renderProduct(i, item, results)
            } else {
                clearProducts()
            }
        }
    } else {
        getProduct(selectedProduct)
    }
}

getProduct("Geotextiles")

function getProduct(product) {
    clearProductTypes()
    clearProducts()
    selectedProduct = product
    let list = Object.keys(productList[product])
    let items = Object.entries(productList[product])

    for (i = 0; i < list.length; i++) {
        let item = items[i][1]
        let types = Object.entries(productList[product])[i][1].type
        setTypes(types, item)
        renderProduct(i, item, results)
    }
}

function setTypes(types, item) {
    let typeFilter = document.querySelector('#productTypes')
    if (!productTypes.includes(types)) {
        productTypes.push(types)
        let type = document.createElement('option')
        type.value = item.type
        type.innerHTML = item.type
        typeFilter.appendChild(type)
    }
}

function renderProduct(i, item, results) {
    setTimeout(() => {
        let product = document.createElement('div')
        product.classList.add('product-body')
        // Product Code
        let productCode = document.createElement('p')
        productCode.innerHTML = item.code
        product.appendChild(productCode)
        // Product Name
        let productName = document.createElement('p')
        productName.innerHTML = item.name
        product.appendChild(productName)
        // Product Size
        let productSize = document.createElement('p')
        productSize.innerHTML = item.size
        product.appendChild(productSize)
        //Product Type
        let productType = document.createElement('p')
        productType.innerHTML = item.type
        product.appendChild(productType)
        //Product Price
        let productPrice = document.createElement('p')
        productPrice.innerHTML = `$${item.price.toFixed(2)}`
        product.appendChild(productPrice)
        results.appendChild(product)
    }, 100 * i);
}

function clearProducts() {
    while(results.firstChild) {
        results.removeChild(results.firstChild)
    }
}

function clearProductTypes() {
    let types = document.querySelector('#productTypes')
    while(types.firstChild) {
        types.removeChild(types.firstChild)
    }
    productTypes = ['All']
    let type = document.createElement('option')
    type.value = "All"
    type.innerHTML = "All"
    types.appendChild(type)
}
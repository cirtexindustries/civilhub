function productSearch(term, parameter) {
    for (x = 0; x < Object.keys(basePrice).length; x++) {
        let productCategory = Object.entries(basePrice)[x][0]
        let items = Object.entries(basePrice[`${productCategory}`])
        for (i = 0; i < items.length; i++) {
            if (items[i][1][`${parameter}`].toLowerCase().includes(term.toLowerCase())) {
                let item = items[i][1]
                console.log(item)
                clearProducts()
                renderProduct(0, item)
            } else {
                clearProducts()
            }
        }
    }
}

function filterProductType(filter) {
    let items = Object.entries(basePrice[selectedProduct])

    if (filter != "All") {
        for (i = 0; i < items.length; i++) {
            if (items[i][1].type.includes(filter)) {
                let item = items[i][1]
                clearProducts()
                renderProduct(i, item)
            } else {
                clearProducts()
            }
        }
    } else {
        getProduct(selectedProduct)
    }
}

